class M{
  public static void main(String[] args){
     boolean a = 10>5 && 10>8;     
     boolean b = 10>5 & 10>8;

     boolean c = 10>5 || 10>18;
     boolean d = 10>5 | 10>30;

     System.out.println(a); 
     System.out.println(b);
     System.out.println(c);
     System.out.println(d);
  }

}