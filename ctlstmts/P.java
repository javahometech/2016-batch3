class P{

// Switch Case
public static void main(String[] args){
    int i = 1;
    switch(i){
     case 1 : System.out.println("One");
              break;

     case 2 : System.out.println("TwO");
              break;

     case 3 : System.out.println("Three");
              break;
    default : System.out.println("Default");
              break;
    }
}

}